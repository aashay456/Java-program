    public static void main(String args[]){
        int a=99;
        // a=a+1;
        a+=1;
        System.out.println(a);
        int b=1000;
        // b=b-999;
        b-=999;
        System.out.println(b);
        int c=10;
        // c=c*10;
        c*=10;
        System.out.println(c);
        int d=200;
        // d=d/25;
        d/=25;
        System.out.println(d);
        int e=5;
        // e=e/5;
        e%=5;
        System.out.println(e);


    }
